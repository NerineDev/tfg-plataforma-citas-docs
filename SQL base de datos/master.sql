-- =========================================================
-- 6. FACTURACIÓN Y PAGOS
-- =========================================================

-- ---------------------------------------------------------
-- 6.1 INVOICES
-- ---------------------------------------------------------
-- Almacena las facturas generadas por el sistema.
--
-- Decisiones importantes de diseño:
-- - La factura se guarda como entidad propia porque forma parte
--   del apoyo contable al profesional.
-- - Puede existir más de una factura asociada a una misma reserva
--   si se producen correcciones, reversiones o sustituciones.
-- - Se distingue entre factura simplificada y factura completa.
-- - Las facturas emitidas no deben modificarse libremente: cuando
--   sea necesario corregirlas, se utilizará la lógica de reversión
--   y sustitución.
CREATE TABLE sesvia.invoices (
    invoice_id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_id int4 NOT NULL,
    billing_detail_id int4,
    invoice_number varchar(50) NOT NULL,
    invoice_series varchar(20),
    invoice_type varchar(20) NOT NULL DEFAULT 'simplified',
    issued_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,

    recipient_name varchar(150),
    recipient_tax_id varchar(30),
    recipient_address text,

    subtotal_amount numeric(10,2) NOT NULL,
    tax_amount numeric(10,2) NOT NULL DEFAULT 0,
    total_amount numeric(10,2) NOT NULL,
    currency_code varchar(10) NOT NULL DEFAULT 'EUR',

    status varchar(20) NOT NULL DEFAULT 'issued',

    is_reversal bool NOT NULL DEFAULT false,
    reverses_invoice_id int4,
    replaces_invoice_id int4,
    correction_reason text,

    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT uq_invoices_number_series
        UNIQUE (invoice_series, invoice_number),

    CONSTRAINT chk_invoices_type
        CHECK (invoice_type IN ('simplified', 'full')),

    CONSTRAINT chk_invoices_status
        CHECK (status IN ('issued', 'reversed', 'cancelled')),

    CONSTRAINT chk_invoice_total_match
        CHECK (total_amount = subtotal_amount + tax_amount),

    CONSTRAINT chk_invoice_full
        CHECK (
            invoice_type <> 'full'
            OR (
                recipient_name IS NOT NULL
                AND recipient_tax_id IS NOT NULL
                AND recipient_address IS NOT NULL
            )
        ),

    CONSTRAINT chk_invoice_simple
        CHECK (
            invoice_type <> 'simplified'
            OR ABS(total_amount) <= 400.00
        ),

    CONSTRAINT chk_invoice_reversal
        CHECK (
            (is_reversal = true AND total_amount <= 0)
            OR
            (is_reversal = false AND total_amount >= 0)
        ),

    CONSTRAINT chk_invoices_correction_flow
        CHECK (
            (is_reversal = false AND reverses_invoice_id IS NULL)
            OR
            (is_reversal = true AND reverses_invoice_id IS NOT NULL)
        ),

    CONSTRAINT fk_invoices_booking
        FOREIGN KEY (booking_id)
        REFERENCES sesvia.bookings(booking_id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_invoices_billing_detail
        FOREIGN KEY (billing_detail_id)
        REFERENCES sesvia.billing_details(billing_detail_id)
        ON DELETE SET NULL,

    CONSTRAINT fk_invoices_reverses_invoice
        FOREIGN KEY (reverses_invoice_id)
        REFERENCES sesvia.invoices(invoice_id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_invoices_replaces_invoice
        FOREIGN KEY (replaces_invoice_id)
        REFERENCES sesvia.invoices(invoice_id)
        ON DELETE RESTRICT
);


-- ---------------------------------------------------------
-- 6.2 PAYMENTS
-- ---------------------------------------------------------
-- Registra los pagos asociados a reservas.
--
-- Decisiones importantes de diseño:
-- - El importe del pago se mantiene no negativo.
-- - Las devoluciones o incidencias de cobro se reflejan mediante
--   el estado del pago y, en el plano contable, mediante la lógica
--   de facturas de reversión cuando corresponda.
-- - El receipt_number se guarda aquí porque el justificante de pago
--   pertenece al evento de pago, no a la reserva ni a la factura.
CREATE TABLE sesvia.payments (
    payment_id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_id int4 NOT NULL,
    invoice_id int4,
    amount numeric(10,2) NOT NULL,
    currency_code varchar(10) NOT NULL DEFAULT 'EUR',
    payment_method varchar(30) NOT NULL,
    status varchar(30) NOT NULL DEFAULT 'pending',
    paid_at timestamptz,
    transaction_ref varchar(100),
    receipt_number varchar(50),
    receipt_issued_at timestamptz,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT uq_payments_receipt
        UNIQUE (receipt_number),

    CONSTRAINT chk_payments_amount
        CHECK (amount >= 0),

    CONSTRAINT chk_payments_method
        CHECK (payment_method IN ('paypal', 'stripe')),

    CONSTRAINT chk_payments_status
        CHECK (status IN ('pending', 'paid', 'failed', 'refunded', 'cancelled')),

    CONSTRAINT fk_payments_booking
        FOREIGN KEY (booking_id)
        REFERENCES sesvia.bookings(booking_id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_payments_invoice
        FOREIGN KEY (invoice_id)
        REFERENCES sesvia.invoices(invoice_id)
        ON DELETE SET NULL
);


-- =========================================================
-- 7. FUNCIONES Y TRIGGERS DE COHERENCIA
-- =========================================================

-- ---------------------------------------------------------
-- 7.1 FUNCIÓN: BOOKING / SERVICE / PRO PROFILE
-- ---------------------------------------------------------
-- Una reserva no puede utilizar un servicio que pertenezca a un
-- perfil profesional distinto del indicado en la propia reserva.
-- Esta regla se refuerza mediante un trigger para evitar
-- incoherencias entre la reserva, el profesional y el servicio.
CREATE OR REPLACE FUNCTION sesvia.fn_check_booking_service_owner()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_service_pro_profile_id int4;
BEGIN
    SELECT pro_profile_id
    INTO v_service_pro_profile_id
    FROM sesvia.services
    WHERE service_id = NEW.service_id;

    IF v_service_pro_profile_id IS NULL THEN
        RAISE EXCEPTION 'The selected service does not exist.';
    END IF;

    IF NEW.pro_profile_id <> v_service_pro_profile_id THEN
        RAISE EXCEPTION 'The selected service does not belong to the selected professional profile.';
    END IF;

    RETURN NEW;
END;
$$;


-- ---------------------------------------------------------
-- 7.2 FUNCIÓN: PAYMENT / INVOICE / BOOKING
-- ---------------------------------------------------------
-- Un pago no puede vincularse a una factura asociada a una reserva
-- distinta. Esta regla se refuerza mediante un trigger para
-- mantener la coherencia entre pago, factura y reserva.
CREATE OR REPLACE FUNCTION sesvia.fn_check_payment_invoice_booking()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_invoice_booking_id int4;
BEGIN
    IF NEW.invoice_id IS NOT NULL THEN
        SELECT booking_id
        INTO v_invoice_booking_id
        FROM sesvia.invoices
        WHERE invoice_id = NEW.invoice_id;

        IF v_invoice_booking_id IS NULL THEN
            RAISE EXCEPTION 'The selected invoice does not exist.';
        END IF;

        IF NEW.booking_id <> v_invoice_booking_id THEN
            RAISE EXCEPTION 'The selected invoice does not belong to the same booking as the payment.';
        END IF;
    END IF;

    RETURN NEW;
END;
$$;


-- ---------------------------------------------------------
-- 7.3 TRIGGERS
-- ---------------------------------------------------------
CREATE TRIGGER trg_booking_service_owner
BEFORE INSERT OR UPDATE OF pro_profile_id, service_id
ON sesvia.bookings
FOR EACH ROW
EXECUTE FUNCTION sesvia.fn_check_booking_service_owner();

CREATE TRIGGER trg_payment_invoice_booking
BEFORE INSERT OR UPDATE OF booking_id, invoice_id
ON sesvia.payments
FOR EACH ROW
EXECUTE FUNCTION sesvia.fn_check_payment_invoice_booking();


