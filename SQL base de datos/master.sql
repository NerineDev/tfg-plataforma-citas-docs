    message_id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    thread_id int4 NOT NULL,
    sender_id int4 NOT NULL,
    body text NOT NULL,
    sent_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
